class Machine {
  int id;
  final String productName;
  int productPrice;
  int productQuantity;
  String productCategory;
  String description;
  String unit;

  Machine({
    required this.id,
    required this.productName,
    required this.productPrice,
    required this.productQuantity,
    required this.productCategory,
    required this.description,
    required this.unit,
  });
}
